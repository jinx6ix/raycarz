// Generate image schema markup for SEO
export function generateImageSchema(imageData: any) {
  return {
    '@context': 'https://schema.org',
    '@type': 'ImageObject',
    url: imageData.contentUrl,
    name: imageData.title,
    description: imageData.description,
    width: imageData.width,
    height: imageData.height,
    encodingFormat: imageData.encodingFormat,
    uploadDate: imageData.uploadDate,
    license: imageData.license,
    creditText: imageData.creditText,
    copyrightNotice: imageData.copyrightNotice,
    keywords: imageData.keywords?.join(', ')
  };
}

